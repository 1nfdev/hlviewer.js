class Vector2 {
    x: number
    y: number

    constructor() {
        this.x = 0
        this.y = 0
    }
}

class Vector3 {
    x: number
    y: number
    z: number

    constructor() {
        this.x = 0
        this.y = 0
        this.z = 0
    }
}

export {
    Vector2,
    Vector3
}
